    private void llenarTabla() {
        AccesoBD acceso = new AccesoBD();
        jtTabla.setVisible(false);
        jtTabla.removeAll();
        jtTabla.setModel(acceso.retornaModelo("Compras", "From Compra"));
        jtTabla.setVisible(true);
    }

    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        try {
            AccesoBD acceso = new AccesoBD();
            pojos.Compra compra = new pojos.Compra();
            compra.setFechaCompra(jdcFecha.getDate());
            compra.setNombreSucursal(cmbSucursal.getSelectedItem().toString());
            compra.setTipoDeGasto(cmbTipoDeGasto.getSelectedItem().toString());
            compra.setNombreProveedor(cmbProveedor.getSelectedItem().toString());
            compra.setMetodoPago(cmbMetodoDePago.getSelectedItem().toString());
            compra.setImporte(Double.parseDouble(txtImporte.getText()));
            compra.setObservacion(txtObservaciones.getText());
            acceso.add(compra);
            llenarTabla();
            limpiarCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e, "Error", 0);
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

